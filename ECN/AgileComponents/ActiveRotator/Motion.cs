using System;

namespace ActiveUp.WebControls
{
	/// <summary>
	/// Motion of the transition.
	/// </summary>
	/// <remarks>Used by Barn, GradientWipe, Iris and Strips transitions.</remarks>
	public enum Motion
	{
		/// <summary>
		/// Not set, will use the default.
		/// </summary>
		NotSet,
		/// <summary>
		/// In motion.
		/// </summary>
		In,
		/// <summary>
		/// Out motion.
		/// </summary>
		Out		
	}
}
