CREATE Proc dbo.e_SurveyStyles_Save           
(            
        @SurveyID int ,        
        @pWidth varchar(500) = NULL ,        
        @pbgcolor varchar(500) = NULL ,        
        @pAlign varchar(500) = NULL ,        
        @pBorder bit ,        
        @pBordercolor varchar(500) = NULL ,        
        @pfontfamily varchar(500) = NULL ,        
        @hImage varchar(500) = NULL ,        
        @hAlign varchar(500) = NULL ,        
        @hMargin varchar(500) = NULL ,        
        @hbgcolor varchar(500) = NULL ,        
        @phbgcolor varchar(500) = NULL ,        
        @phfontsize varchar(500) = NULL ,        
        @phcolor varchar(500) = NULL ,        
        @phBold bit ,        
        @pdbgcolor varchar(500) = NULL ,        
        @pdfontsize varchar(500) = NULL ,        
        @pdcolor varchar(500) = NULL ,        
        @pdbold bit ,        
        @bbgcolor varchar(500) = NULL ,        
        @qcolor varchar(500) = NULL ,        
        @qfontsize varchar(500) = NULL ,        
        @qbold bit ,        
        @acolor varchar(500) = NULL ,        
        @abold bit ,        
        @afontsize varchar(500) = NULL ,        
        @fImage varchar(500) = NULL ,        
        @fAlign varchar(500) = NULL ,        
        @fMargin varchar(500) = NULL ,        
        @fbgcolor varchar(500) = NULL ,        
        @ShowQuestionNo bit
)    
as    
Begin    

update SurveyStyles set   
					 pAlign =   @pAlign  , 
					 pbgcolor =   @pbgcolor  , 
					 pBorder =   @pBorder , 
					 pBordercolor =   @pBordercolor  , 
					 pfontfamily =   @pfontfamily  , 
					 pWidth =   @pWidth  , 
					 hImage =   @hImage  , 
					 hAlign =   @hAlign  , 
					 hbgcolor =   @hbgcolor  , 
					 hMargin =  @hMargin, 
					 phbgcolor =   @phbgcolor  , 
					 phBold =   @phBold , 
					 phcolor =   @phcolor  , 
					 phfontsize =   @phfontsize  , 
					 pdbgcolor =   @pdbgcolor  , 
					 pdBold =   @pdbold  , 
					 pdcolor =   @pdcolor  , 
					 pdfontsize =   @pdfontsize  , 
					 bbgcolor =   @bbgcolor  , 
                     ShowQuestionNo =   @showquestionno, 
					 qBold = @qbold  , 
					 qcolor =   @qcolor  , 
					 qfontsize =   @qfontsize  , 
					 aBold =   @abold  , 
					 acolor =   @acolor  , 
					 afontsize =   @afontsize  , 
					 fImage =   @fImage  , 
					 fAlign =   @falign  , 
					 fbgcolor =   @fbgcolor  , 
					 fMargin =   @fMargin
					 where SurveyID =   @SurveyID
End