CREATE PROCEDURE dbo.e_ApplicationLog_Update
@LogID int,
@ApplicationID int = -1,
@SeverityID int = 1,
@SourceMethod nvarchar(250),
@Exception text,
@LogNote text,
@IsBug bit,
@IsUserSubmitted bit,
@GDCharityID int,
@ECNCustomerID int,
@SubmittedBy nvarchar(250),
@SubmittedByEmail nvarchar(100),
@IsFixed bit,
@FixedDate date,
@FixedTime time(7),
@FixedBy nvarchar(100),
@FixedNote nvarchar(750)
AS
UPDATE ApplicationLog 
SET ApplicationID = @ApplicationID,
	SeverityID = @SeverityID,
    SourceMethod = @SourceMethod,
    Exception = @Exception,
    LogNote = @LogNote,
    IsBug = @IsBug,
    IsUserSubmitted = @IsUserSubmitted,
    GDCharityID = @GDCharityID,
    ECNCustomerID = @ECNCustomerID,
    SubmittedBy = @SubmittedBy,
    SubmittedByEmail = @SubmittedByEmail,
    IsFixed = @IsFixed,
    FixedDate = @FixedDate,
    FixedTime = @FixedTime,
    FixedBy = @FixedBy,
    FixedNote = @FixedNote,
    LogUpdatedDate = GETDATE(),
    LogUpdatedTime = GETDATE()
WHERE LogID = @LogID
