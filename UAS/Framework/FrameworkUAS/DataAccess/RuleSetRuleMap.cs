using System;
using System.Collections.Generic;
using System.Data;
using System.Data.SqlClient;
using KM.Common;
using KM.Common.Data;

namespace FrameworkUAS.DataAccess
{
    public class RuleSetRuleMapSetRuleSetRuleMapMap
    {
        private static Entity.RuleSetRuleMap Get(SqlCommand cmd)
        {
            Entity.RuleSetRuleMap retItem = null;
            try
            {
                using (SqlDataReader rdr = KM.Common.DataFunctions.ExecuteReader(cmd, ConnectionString.UAS.ToString()))
                {
                    if (rdr != null)
                    {
                        retItem = new Entity.RuleSetRuleMap();
                        DynamicBuilder<Entity.RuleSetRuleMap> builder = DynamicBuilder<Entity.RuleSetRuleMap>.CreateBuilder(rdr);
                        while (rdr.Read())
                        {
                            retItem = builder.Build(rdr);
                        }
                        rdr.Close();
                        rdr.Dispose();
                    }
                }
            }
            catch { }
            finally
            {
                cmd.Connection.Close();
                cmd.Dispose();
            }
            return retItem;
        }
        private static List<Entity.RuleSetRuleMap> GetList(SqlCommand cmd)
        {
            List<Entity.RuleSetRuleMap> retList = new List<Entity.RuleSetRuleMap>();
            try
            {
                using (SqlDataReader rdr = KM.Common.DataFunctions.ExecuteReader(cmd, ConnectionString.UAS.ToString()))
                {
                    if (rdr != null)
                    {
                        Entity.RuleSetRuleMap retItem = new Entity.RuleSetRuleMap();
                        DynamicBuilder<Entity.RuleSetRuleMap> builder = DynamicBuilder<Entity.RuleSetRuleMap>.CreateBuilder(rdr);
                        while (rdr.Read())
                        {
                            retItem = builder.Build(rdr);
                            if (retItem != null)
                            {
                                retList.Add(retItem);
                            }
                        }
                        rdr.Close();
                        rdr.Dispose();
                    }
                }
            }
            catch { }
            finally
            {
                cmd.Connection.Close();
                cmd.Dispose();
            }
            return retList;
        }
    }
