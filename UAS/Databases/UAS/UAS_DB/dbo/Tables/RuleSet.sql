CREATE TABLE [dbo].[RuleSet]
(
	RuleSetId int identity(1,1) NOT NULL,
	RuleSetName varchar(250) not null,
	DisplayName varchar(250) not null,
	RuleSetDescription varchar(1250) null,
	IsActive bit not null,
	IsSystem bit not null,
	IsGlobal bit default ((0)) not null,
	ClientId int not null,
	IsDateSpecific bit default((0)) not null,
	StartMonth int null,
	EndMonth int null,
	StartDay int null,
	EndDay int null,
	StartYear int null,
	EndYear int null,
	DateCreated datetime default(getdate()) not null,
	DateUpdated datetime null,
	CreatedByUserId int not null,
	UpdatedByUserId int null, 
	CustomImportRuleId int DEFAULT ((0)) not null,
    CONSTRAINT [PK_RuleSet] PRIMARY KEY ([RuleSetId]) WITH (FILLFACTOR = 90), 
)
